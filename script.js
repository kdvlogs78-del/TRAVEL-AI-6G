// ─── Haversine distance (km) ─────────────────────────────────────
function haversineKm(lat1, lon1, lat2, lon2) {
    const R = 6371;
    const dLat = (lat2 - lat1) * Math.PI / 180;
    const dLon = (lon2 - lon1) * Math.PI / 180;
    const a = Math.sin(dLat / 2) ** 2 +
              Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
              Math.sin(dLon / 2) ** 2;
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

// ─── Find nearest city from the full list ────────────────────────
function findNearestCity(lat, lng) {
    let nearest = null, minDist = Infinity;
    Object.entries(CITY_COORDS).forEach(([city, [clat, clng]]) => {
        const dist = haversineKm(lat, lng, clat, clng);
        if (dist < minDist) { minDist = dist; nearest = city; }
    });
    return nearest;
}

// ─── CURRENT LOCATION ────────────────────────────────────────────
function useCurrentLocation() {
    const btn = document.getElementById('useLocationBtn');
    const btnText = document.getElementById('locationBtnText');
    const fromInput = document.getElementById('wizFromInput');
    if (!navigator.geolocation) { showToast('Geolocation not supported.', 'error'); return; }
    btn.classList.add('loading');
    btnText.textContent = 'Detecting...';
    navigator.geolocation.getCurrentPosition(
        (position) => {
            const { latitude, longitude } = position.coords;
            const nearestCity = findNearestCity(latitude, longitude);
            if (nearestCity) {
                fromInput.value = nearestCity;
                wizState.from = nearestCity;
                fromInput.style.transition = 'box-shadow 0.3s, border-color 0.3s';
                fromInput.style.borderColor = '#ABD1C6';
                fromInput.style.boxShadow = '0 0 0 3px rgba(171,209,198,0.35)';
                setTimeout(() => { fromInput.style.borderColor = ''; fromInput.style.boxShadow = ''; }, 2500);
                showToast('\uD83D\uDCCD Nearest city: ' + nearestCity, 'success');
            } else {
                showToast('\uD83D\uDCCD Could not detect city. Please type manually.', 'error');
            }
            btn.classList.remove('loading');
            btnText.textContent = nearestCity ? ('\u2713 ' + nearestCity) : '\uD83D\uDCCD Detect My City (sets From)';
            setTimeout(() => { btnText.textContent = '\uD83D\uDCCD Detect My City (sets From)'; }, 4000);
        },
        (err) => {
            btn.classList.remove('loading');
            btnText.textContent = '\uD83D\uDCCD Detect My City (sets From)';
            const msgs = {1:'Location access denied.',2:'Unable to detect location.',3:'Request timed out.'};
            showToast(msgs[err.code] || 'Could not get location.', 'error');
        },
        { timeout: 10000, enableHighAccuracy: false }
    );
}


